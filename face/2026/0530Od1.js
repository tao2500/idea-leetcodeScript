// 题目描述 在一个数字游戏中，m（1≤m≤105） 个参与者排成一队，每人持有一个整数号码牌 n（0≤n≤109）。
// 游戏只进行一轮，按照初始队列的顺序（从队首到队尾）处理每个人，每人仅处理一次，处理规则如下：
//      如果号码是 3 的倍数，则将其淘汰（从队列中移除）。
//      否则，如果号码的十进制表示中包含数字 3，则将其移动到队尾。
//      否则，如果号码的十进制表示中包含数字 2，则将其移动到队首。
//      否则，位置保持不变。

//      对于规则 2、3：不论插入队首还是队尾，他们的相对顺序与他们在初始队列中的相对顺序保持一致。
//优先级说明：
//  如果一个数字同时满足规则 1 和规则 2，或同时满足规则 1 和规则 3，则遵从规则 1（淘汰）。
//  如果一个数字同时满足规则 2 和规则 3（即同时包含数字 2 和 3，且不是 3 的倍数），则遵从规则 2（移动到队尾）。

//输入描述
//  给定一个表示所有排成队列的链表的头 ListNode

//输出描述
//  1.输出一行整数，表示最终队列的号码牌顺序，用空格分隔
//  2.超过号码牌整数范围或者人数超过总人数限制，返回 “−1”

//示例1
// 输入
// 12,23,7,13,8
// 输出
// 7 8 23 13

// 说明
// 处理过程：
// 淘汰 12→[23,7,13,8]
// 23 移动到队尾 →[7,13,8,23]
// 7 不变 →[7,13,8,23]
// 13 移动到队尾 →[7,8,23,13]
// 8 不变 →[7,8,23,13]
// 最终顺序为 7 8 23 13。



/**
 * 链表数字游戏
 *
 * @param {ListNode} head - 链表头节点
 * @returns {string} 最终队列或 "-1"
 */
function gameResult(head) {
    // front: 包含数字2，移动到队首
    // middle: 既不淘汰也不移动
    // back: 包含数字3（但不是3的倍数），移动到队尾
    const front = [];
    const middle = [];
    const back = [];

    let cur = head;
    while (cur !== null) {
        const x = cur.val;

        // 非法数字：超出范围
        if (x < 0 || x > 1000000000) {
            return "-1";
        }

        // 规则1：3的倍数直接淘汰
        if (x % 3 === 0) {
            cur = cur.next;
            continue;
        }

        const s = String(x);

        // 规则2优先于规则3：包含数字3移动到队尾
        if (s.includes('3')) {
            back.push(s);
        }
        // 规则3：包含数字2移动到队首
        else if (s.includes('2')) {
            front.push(s);
        }
        // 规则4：位置保持不变
        else {
            middle.push(s);
        }

        cur = cur.next;
    }

    // 合并结果：队首 + 中间 + 队尾
    const ans = [...front, ...middle, ...back];

    // 没有剩余参与者
    if (ans.length === 0) {
        return "-1";
    }

    return ans.join(' ');
}

